import mongoose from 'mongoose';


const userSchema = mongoose.Schema({
    name: {
        type: String,
        require: true
    },
    email: {
        type: String,
        require: true,
        unique: true
    },
    password: {
        type: String,
        require: true
    },
    avatar: {
        type: String
    }
});


const Users = mongoose.model('user', userSchema);

export default Users;